<h2>Premier « vrai/faux » de notre série, découvrez quelques vérités souvent erronées sur un sujet plus que jamais d’actualité : l’alimentation équilibrée !</h2>

<h3>Les fruits sont plus digestes à la fin du repas</h3>

<p><span class='astuces'>FAUX :</span> ils sont plus digestes entre les repas. Alors n’hésitez plus en cas de petite faim, pensez aux fruits. C’est plein de vitamines, de fibres… et très pauvre en calories.</p>
<h3>Le dernier repas avant un effort doit être copieux</h3>

<p><span class='astuces1'>VRAI :</span> (mais pas trop quand même), pris 3 heures avant l’effort ET digeste ! On évitera donc les graisses animales et cuites (plus difficiles à digérer) ainsi que les aliments qui provoquent ballonnements et lourdeurs.</p>
<h3>Mieux vaut craquer pour une pâtisserie à midi qu’au goûter</h3>

<p><span class='astuces1'>VRAI :</span> Si on souhaite consommer un dessert, il vaut mieux le prendre en fin de repas : mélangé aux autres aliments, les sucres qu’il contient seront métabolisés moins vite et perturberont moins la glycémie. S’il est pris en dehors des repas, ses sucres vont être absorbés plus rapidement et perturber la glycémie. Un peu plus tard, ils vont provoquer une hypoglycémie et donner à nouveau envie de sucré.</p>
<h3>Une assiette équilibrée contient 50 % de végétal</h3>
<p><span class='astuces'>FAUX :</span> elle devrait être composée aux 2/3 de végétal. Un bon repère pour avoir une idée de la portion idéale de viande ? Comptez l’équivalent d’un jeu de cartes.</p>
<h3>Mâcher est bon pour la santé</h3>

<p><span class='astuces1'>VRAI : </span>la mastication est bénéfique car elle permet de libérer les composés alimentaires dans la bouche (molécules responsables du goût et de l’ingestion des nutriments) et contribue à déclencher la sensation de satiété au bout d’une vingtaine de minutes. Par ailleurs, une mastication insuffisante peut engendrer des acidités gastriques alors pensez à bien mâcher !</p>
<h3>Il faut faire le plein d’énergie avant d’aller dormir</h3>

<p><span class='astuces'>FAUX :</span> le corps profite du repos de la nuit pour se nettoyer des toxines accumulées dans la journée. Si on le surcharge au dîner, il passera sa nuit à digérer au lieu d’éliminer…</p>
<h3>Tous les lipides se valent</h3>

<p><span class='astuces'>FAUX :</span> Certains sont à éviter et d’autres sont indispensables. Les fameux Oméga 3 et Oméga 6  font partie des « Acides Gras Essentiels » à notre organisme car il ne peut les fabriquer lui-même. Il est donc important de consommer un poisson gras (saumon, sardine, maquereau, thon) deux fois par semaine et certaines huiles végétales quotidiennement (colza, noix, soja, lin, chanvre).</p>
