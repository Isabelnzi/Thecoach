<p>Le + nutrition 
Pourquoi c’est bon ?

Ce petit déjeuner pauvre en graisses saturées vous fournit le carburant nécessaire avant un effort : une bonne combinaison de glucides simples et complexes, des protéines de bonne qualité nutritionnelle, des fibres, les vitamines anti-oxydantes des fruits et des minéraux comme le calcium et le potassium, importants pour l’effort. Ci-dessous, la recette complète pour 2 personnes.
Les ingrédients
Pour 2 personnes

    500 ml de boisson  SOJASUN Amande
    1 sachet de sucre vanillé (à remplacer par une Cas de sirop d’érable et une pincée de vanille en poudre pour plus de gourmandise)
    60 g de perles du japon
    1 orange
    1 kiwi
    100 g d’ananas
    10 g de fruits secs (noix, amande, noisette)

La préparation 

    Faire chauffer la boisson avec le sucre vanillé.
    Ajouter les perles du japon et laisser épaissir à feu doux environ 12 à 15 minutes en remuant.
    Verser dans les bols.
    Laisser refroidir et ajouter les fruits coupés et les fruits concassés.

Astuces

Idéalement, à commencer la veille au soir, pour déguster frais au petit déjeuner du lendemain. Voilà un « porridge overnight » sympa pour un réveil frais et gourmand ! Vous pouvez également vous faire plaisir en testant cette recette avec d’autres fruits ou  d’autres boissons  SOJASUN !
